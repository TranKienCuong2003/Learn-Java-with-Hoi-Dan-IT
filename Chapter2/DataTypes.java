package Chapter2;

public class DataTypes {
    static int myNum = 5; // Integer
    static float myFloatNum = 5.99f; // Floating point number
    static char myLetter = 'D'; // Character
    static boolean myBool = true; // Boolean
    static String myText = "Hello"; // String
    
        public static void main(String[] args) {
            System.out.println(myNum);
            System.out.println(myFloatNum);
            System.out.println(myLetter);
            System.out.println(myBool);
            System.out.println(myText);
    }
}
